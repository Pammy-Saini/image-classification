@app.route('/api/classify', methods=['POST'])
def classify():
    data = request.json or {}
    region = data.get('region', 'jodhpur')
    year = data.get('year', '2024')
    algorithm = data.get('algorithm', 'random_forest')
    
    # Algorithm parameters
    k_clusters = int(data.get('k_clusters', 5))
    n_estimators = int(data.get('n_estimators', 50))
    svm_c = float(data.get('svm_c', 1.0))
    
    try:
        bands, _ = load_bands_and_ground_truth(region, year)
        
        if algorithm == 'kmeans':
            classified_map = classify_kmeans(bands, k_clusters)
        else: # random_forest or svm
            x_train, y_train = get_simulated_training_data(region, year)
            classified_map = classify_supervised(bands, x_train, y_train, algorithm, n_estimators, svm_c)
            
        metrics = get_class_metrics(classified_map)
        img_base64 = create_color_map_base64(classified_map)
        
        return jsonify({
            'success': True,
            'region': region,
            'year': year,
            'algorithm': algorithm,
            'metrics': metrics,
            'image': f"data:image/png;base64,{img_base64}"
        })
    except Exception as e:
        return jsonify({'success': False, 'error': str(e)}), 500
@app.route('/api/change_detection', methods=['POST'])
def change_detection():
    data = request.json or {}
    region = data.get('region', 'jodhpur')
    algorithm = data.get('algorithm', 'random_forest')
    
    # Parameters
    k_clusters = int(data.get('k_clusters', 5))
    n_estimators = int(data.get('n_estimators', 50))
    svm_c = float(data.get('svm_c', 1.0))
    
    try:
        bands_2024, _ = load_bands_and_ground_truth(region, '2024')
        bands_2025, _ = load_bands_and_ground_truth(region, '2025')
        
        if algorithm == 'kmeans':
            map_2024 = classify_kmeans(bands_2024, k_clusters)
            map_2025 = classify_kmeans(bands_2025, k_clusters)
        else:
            # Train and classify 2024
            x_tr_24, y_tr_24 = get_simulated_training_data(region, '2024')
            map_2024 = classify_supervised(bands_2024, x_tr_24, y_tr_24, algorithm, n_estimators, svm_c)
            # Train and classify 2025
            x_tr_25, y_tr_25 = get_simulated_training_data(region, '2025')
            map_2025 = classify_supervised(bands_2025, x_tr_25, y_tr_25, algorithm, n_estimators, svm_c)
            
        metrics_2024 = get_class_metrics(map_2024)
        metrics_2025 = get_class_metrics(map_2025)
        
        # Calculate transition matrix (change grid)
        # We compute how many pixels shifted from class A to class B
        transition_matrix = np.zeros((len(CLASS_KEYS), len(CLASS_KEYS)), dtype=int)
        for r in range(SIZE):
            for c in range(SIZE):
                c_24 = map_2024[r, c]
                c_25 = map_2025[r, c]
                transition_matrix[c_24, c_25] += 1
                
        # Format transition matrix for response
        transition_data = []
        for r_idx, from_key in enumerate(CLASS_KEYS):
            row_data = {}
            for c_idx, to_key in enumerate(CLASS_KEYS):
                pixel_count = int(transition_matrix[r_idx, c_idx])
                area_ha = (pixel_count * PIXEL_AREA_SQ_M) / 10000.0
                row_data[to_key] = {
                    'pixels': pixel_count,
                    'area_ha': round(area_ha, 2)
                }
            transition_data.append({
                'from_id': from_key,
                'from_name': CLASS_INFO[from_key]['name'],
                'transitions': row_data
            })
            
        # Calculate net changes
        net_changes = []
        for m24, m25 in zip(metrics_2024, metrics_2025):
            diff_ha = m25['area_ha'] - m24['area_ha']
            diff_pct = m25['percentage'] - m24['percentage']
            net_changes.append({
                'id': m24['id'],
                'name': m24['name'],
                'color': m24['color'],
                'area_2024': m24['area_ha'],
                'area_2025': m25['area_ha'],
                'diff_ha': round(diff_ha, 2),
                'diff_pct': round(diff_pct, 2)
            })
            
        img_24_b64 = create_color_map_base64(map_2024)
        img_25_b64 = create_color_map_base64(map_2025)
        
        return jsonify({
            'success': True,
            'region': region,
            'algorithm': algorithm,
            'metrics_2024': metrics_2024,
            'metrics_2025': metrics_2025,
            'image_2024': f"data:image/png;base64,{img_24_b64}",
            'image_2025': f"data:image/png;base64,{img_25_b64}",
            'net_changes': net_changes,
            'transition_matrix': transition_data
        })
    except Exception as e:
        return jsonify({'success': False, 'error': str(e)}), 500
